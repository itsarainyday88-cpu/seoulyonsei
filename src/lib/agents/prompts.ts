// 4. [SYSTEM BUILDER] 최종 프롬프트 조립기
// ============================================================================

// 분리된 상수들을 통합 객체로 매핑
export const AGENT_PROMPTS: Record<string, string> = {

   Marketer: MARKETER_AGENT_PROMPT,
   Blog: BLOG_AGENT_PROMPT,
   Insta: INSTA_AGENT_PROMPT,
   Dang: COMMUNITY_AGENT_PROMPT,
   Supporter: SUPPORTER_AGENT_PROMPT,
   Reputation: REPUTATION_AGENT_PROMPT,
};

export function getSystemInstruction(agentId: string, userMessage: string = '') {
   // 1. 에이전트별 특수 역할 (Individual)
   const specificPrompt = AGENT_PROMPTS[agentId] || AGENT_PROMPTS.Marketer;

   // 2. 전역 시스템 규칙 (Global Rules)
   const globalRules = GLOBAL_RULES_FOR_ALL_AGENTS;

   // 3. 비즈니스 팩트 (Global Fact)
   const businessContext = BUSINESS_INFO;

   // 4. 리소스 가이드 (Global Manual)
   const resourceContext = RESOURCE_INSTRUCTIONS;

   // 5. 블로그 전용 문구 (Conditional)
   const blogOnlyContext = agentId === 'Blog' ? BLOG_ONLY_CONTENTS : "";

   // [FINAL ASSEMBLY]
   // 모든 에이전트에 공통 적용: 검색 기반 사실 확인 SOP
   const globalSearchSOP = `
[🔍 MANDATORY: 검색 기반 사실 확인 SOP (모든 에이전트 공통)]
⚠️ 너는 정보를 절대 지어내거나 추측으로 작성해서는 안 된다. 아래 프로토콜을 반드시 따르라.

[도구 목록]
1. **search_local_trends(query, exclude_domains)**: 키워드로 최신 정보/URL 리스트 검색.
2. **scrape_website(url)**: 특정 URL의 본문을 추출하여 실제 내용을 확인.

[🚨 사실 확인 필수 규칙]
- 수능, 입시, 의대, 법령, 정책, 통계, 날짜, 인물, 사건 등 **사실성이 중요한 모든 정보**는 반드시 search_local_trends로 검색한 후 작성하라.
- 자신의 학습 데이터가 오래되었을 수 있음을 인지하라. 확신이 없으면 검색 먼저.
- 검색 결과 없이 사실 주장을 하는 것은 금지다. 검색 후에도 출처가 불분명하면 "확인이 필요합니다"라고 명시하라.
- **우리 학원 블로그(blog.naver.com/seoul_yonsei)는 항상 exclude_domains에 포함**하여 자사 글 스크래핑을 방지하라.

[자율 실행 프로토콜]
- 사실 기반 정보(입시/정책/통계 등)를 다루는 요청이면:
  1. search_local_trends로 관련 최신 정보를 검색한다.
  2. 수집된 URL 중 신뢰도 높은 2~3개를 scrape_website로 순차적으로 읽는다.
  3. 검색된 사실만을 근거로 내용을 작성한다.
`;

   // 마케터 전용 추가 지시 (로컬 트렌드 분석)
   const marketerAddOn = agentId === 'Marketer' ? `
[📊 Marketer 전용 추가: 로컬 트렌드 분석]
- 경쟁 학원 블로그, 지역 입시 트렌드 분석 시 search_local_trends를 적극 활용하라.
- 검색어 예시: "김포 수학학원", "운양동 국어학원 후기", "2025 수능 트렌드" 등
` : '';

   return `${globalRules}\n${businessContext}\n${blogOnlyContext}\n${resourceContext}\n${MCP_MANUALS.COMPLIANCE_CHECK}\n${globalSearchSOP}\n${marketerAddOn}\n\n[CURRENT AGENT PROFILE]\n${specificPrompt}`;
}
